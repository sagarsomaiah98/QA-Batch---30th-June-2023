package ArithmeticOperators;

public class BasicMath {
	
	//Arithmetic operators are  +, -, *, /, % (MOD).

	public static void main(String[] args) {
		
		int a=27;
		int b=10;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);//divisor
		System.out.println(a%b);//remainder
		

	}

}
